package com.example.lab4v3;

public class PointCredentialsRequest {
    private float X;
    private float Y;
    private float R;

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }

    public float getR() {
        return R;
    }

    public void setR(float r) {
        R = r;
    }
}
